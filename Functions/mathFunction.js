let number1 = Math.random()
//Math.random() generates random numbers between 0 -1
console.log(number1);
let number2 = Math.random() * 5
//the Math.random() * 5 returns numbers between 0.0000-4.999
console.log(number2);

let number3 = Math.floor(6.6545)
//the Math.floor removes any decimal number and returns a whole number
console.log(number3);

let number4 = Math.floor(Math.random() * 10)
//returns any whole number between 0-9
console.log(number4);

let number5 = Math.floor(Math.random() * 10) + 1
//returns any whole number between 0-9
console.log(number5);